							<h2 class="post-title">How to ditch the debt and start saving</h2><br>
                            <div class="blog-thumbnail mb-50">
                                <img src="content/Wallpapers/saving.jpg" alt="">
                            </div>
                            <div class="blog-content">
                              
                                <p>If you’re having a winter money slump, you need to start planning the best Christmas present you can give yourself: financial freedom.

                                If you’re struggling with your finances, you are not alone. According to a survey, which DebtSafe ran in the first quarter of this year, most people’s budgets are stretched, and 51% of respondents in the survey indicated they couldn’t make debt repayments, and 41% overspent on their credit cards.These two statistics show that there’s little extra cash to go around. If consumers don’t have sufficient funds to make payments on debt, the chances are low that they will be saving money. “The average South African is paying almost all of their income on debt repayments and we are on a mission to educate individuals to become better off financially,” according to Debtsafe manager, Wikus Olivier. </p>
								
                                <p>Since anything digital can be copied over and over again, the hard part about implementing a digital payment system is making sure that nobody spends the same money more than once. Traditionally, this is done by having a trusted central authority (like PayPal) that verifies all of the transactions. The core innovation that makes Bitcoin special is that it uses consensus in a massive peer-to-peer network to verify transactions. This results in a system where payments are non-reversible, accounts cannot be frozen, and transaction fees are much lower.

                            	<h5>More than half of people have no savings</h5>
                                <p>It comes as no surprise that 55% of survey participants had no savings plan in place, and says Olivier, a savings plan is essential if you want to buy something big such as a car or a house or save for your retirement.

                                Results from a second survey which ran from March to May this year, show that 53.3% of the respondents can’t save, because they don’t have sufficient funds to cover monthly expenses and savings. </p>
								
								<h5>Keep a record of every cent you spend</h5>
								<p>One good way of finding out where your money is going is jotting down every cent you spend and what you spend it on for a one-month period. Include everything, from tips to car guards, to tuck shop money for the kids.</p>
								<p>It is important to keep track of your financial situation throughout the month, and if you overspend, adjust your budget accordingly. Becoming free from debt means cutting your expenditure and trying to simplify the way you live.</p>
								<p>Always make time for fun, says Olivier. Do things that cost little or nothing, such as going for walks, exercising at home, play with your kids, helping others, and just generally spend more time with people you care about. This will relieve some of the stress you are feeling about your financial situation, and also remind of the important things in your life money cannot buy. </p>
                                <p> Find out exactly how much you earn and make an effort to live within your means.
                                    Draw up a budget for yourself and your household, and stick to it.
                                    If your expenses are too high, some household costs will have to be cut.
                                    If you can’t afford to pay your clothing accounts, arrange for a settlement and cut up your cards.
                                    Avoid taking on all new debt unless you absolutely have to.
                                    Avoid micro-loans if you can’t afford the repayment. Also these interest payments can be financially crippling.
                                    Find out where you can invest your money where you will earn a good return.
                                    Teach your children to become credit savvy.
                                    Think of the peace of mind you will have if you know you can pay all your accounts and still have some savings.
                                   </p>


